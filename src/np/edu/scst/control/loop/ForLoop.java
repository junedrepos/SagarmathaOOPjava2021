package np.edu.scst.control.loop;

public class ForLoop {
    public static void main(String[] args) {
        //for(initialization; condition; increment/decrement)
        for( int i = 0;              i<10;                     i++){
            System.out.println("hello world");
        }
        
        
        for( int i = 0;              i<4;                    ++i){
            System.out.println(i);
        }
    }
   
}
