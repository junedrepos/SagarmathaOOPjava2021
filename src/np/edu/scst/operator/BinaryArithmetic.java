package np.edu.scst.operator;

public class BinaryArithmetic {
   
    //psvm tab
    public static void main(String[] args) {
       int a=2, b=5;
       //binary operator requiers 2 operands
        System.out.println( b + a);//7
        System.out.println(b-a);//3
        System.out.println(b*a);//10
        System.out.println(b/a);//2.5
        System.out.println(b%a);//2
        System.out.println(b^a);//25
    }
}
